-- =====================================================
-- Задача: Извлечение первого слова в верхнем регистре
-- Цель: Преобразовать названия товаров, оставив только первое слово в верхнем регистре

-- Используемые функции:
-- UPPER() - преобразование строки в верхний регистр
-- SPLIT_PART() - разделение строки по разделителю и извлечение части
-- =====================================================


SELECT name,
       UPPER(SPLIT_PART(name, ' ', 1)) as first_word,
       price
FROM   products
ORDER BY name