-- =====================================================
-- Задача: Форматирование информации о заказах
-- Цель: Сформировать читабельную информацию о заказах в заданном формате

-- Используемые функции:
-- CONCAT() - объединение строк
-- DATE() - извлечение даты из временной метки
-- LIMIT - ограничение количества записей
-- =====================================================


SELECT 
  CONCAT(
    'Заказ № ', 
    order_id, 
    ' создан ', 
    DATE(creation_time)
  ) AS order_info 
FROM 
  orders 
LIMIT 
  200