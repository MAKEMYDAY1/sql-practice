-- =====================================================
-- Задача: Расчет повышения цен с округлением
-- Цель: Рассчитать новую цену товаров после повышения на 5% с округлением до 1 знака

-- Используемые функции:
-- ROUND() - округление числовых значений
-- =====================================================

SELECT 
  product_id, 
  name, 
  ROUND(price, 1) AS old_price, 
  ROUND(price * 1.05, 1) AS new_price 
FROM 
  products 
ORDER BY 
  new_price DESC, 
  product_id