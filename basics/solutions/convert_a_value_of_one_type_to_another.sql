-- =====================================================
-- Задача: Изменить тип колонки на VARCHAR.
-- Цель: Измените тип колонки price из таблицы products на VARCHAR.

-- Используемые функции:
-- CAST() - преобразование из одного типа данных в другой
-- =====================================================


SELECT
  name,
  price,
  CAST(price AS VARCHAR) AS price_char
FROM
  products
ORDER BY
  name