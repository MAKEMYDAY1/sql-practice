-- =====================================================
-- Задача: Расчет повышения цен на 5%
-- Цель: Рассчитать новую цену товаров после повышения на 5%

-- Используемые операции:
-- Арифметические операции (умножение)
-- =====================================================

SELECT 
  product_id, 
  name, 
  price AS old_price, 
  price * 1.05 AS new_price 
FROM 
  products 
ORDER BY 
  new_price DESC, 
  product_id
