-- =====================================================
-- Задача: Извлечение года рождения курьеров
-- Цель: Получить год рождения курьеров из даты и отсортировать по возрасту и ID

-- Используемые функции:
-- DATE_PART() - извлечение части из даты (год, месяц, день)
-- ORDER BY - сортировка
-- =====================================================

SELECT 
  courier_id, 
  DATE_PART('year', birth_date) AS birth_year 
FROM 
  couriers 
ORDER BY 
  birth_year DESC, 
  courier_id