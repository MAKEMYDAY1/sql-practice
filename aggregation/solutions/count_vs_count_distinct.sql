-- =====================================================
-- Задача: Анализ пользовательской активности
-- Тема: Агрегация данных в SQL
-- Цель: Сравнить общее количество действий с количеством уникальных пользователей

-- Используемые функции:
-- COUNT(column) - подсчет не-NULL значений в колонке
-- COUNT(DISTINCT column) - подсчет уникальных не-NULL значений
-- =====================================================

SELECT 
  COUNT(user_id) AS users, 
  COUNT(DISTINCT user_id) AS unique_users 
FROM 
  user_actions
