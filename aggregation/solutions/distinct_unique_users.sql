-- =====================================================
-- Задача: Получение уникальных пользователей
-- Тема: Агрегация данных в SQL
-- Цель: Найти всех уникальных пользователей, совершавших действия в системе

-- Используемые операторы:
-- DISTINCT - удаление дубликатов и получение уникальных значений
-- ORDER BY - сортировка результатов
-- =====================================================

SELECT 
  DISTINCT user_id 
FROM 
  user_actions 
ORDER BY 
  user_id
