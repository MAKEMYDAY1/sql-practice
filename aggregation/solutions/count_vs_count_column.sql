-- =====================================================
-- Задача: Анализ полноты данных о датах рождения
-- Тема: Агрегация данных в SQL
-- Цель: Проанализировать заполненность поля даты рождения в пользовательской базе

-- Используемые функции:
-- COUNT(*) - подсчет всех строк в таблице
-- COUNT(column) - подсчет строк с не-NULL значениями в указанной колонке
-- =====================================================

SELECT 
  COUNT(*) AS dates, 
  COUNT(birth_date) AS dates_not_null 
FROM 
  users
