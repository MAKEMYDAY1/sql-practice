-- =====================================================
-- Задача: Расчет стоимости составного заказа
-- Тема: Агрегация данных в SQL
-- Цель: Рассчитать общую стоимость заказа, включающего три конкретных товара

-- Используемые функции:
-- SUM() - агрегатная функция для сложения значений
-- WHERE с IN - фильтрация по списку товаров
-- =====================================================

SELECT 
  SUM(price) AS order_price 
FROM 
  products 
WHERE 
  name IN (
    'сухарики', 'чипсы', 
    'энергетический напиток'
  )
