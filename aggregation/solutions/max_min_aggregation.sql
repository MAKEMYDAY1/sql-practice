-- =====================================================
-- Задача: Анализ ценового диапазона товаров
-- Тема: Агрегация данных в SQL
-- Цель: Определить самый дорогой и самый дешевый товар в ассортименте

-- Используемые функции:
-- MAX() - нахождение максимального значения
-- MIN() - нахождение минимального значения
-- =====================================================

SELECT 
  MAX(price) AS max_price, 
  MIN(price) AS min_price 
FROM 
  products
