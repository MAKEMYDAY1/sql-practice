-- =====================================================
-- Задача: Подсчет курьеров по полу
-- Тема: Агрегация данных в SQL
-- Цель: Определить количество курьеров женского пола в сервисе

-- Используемые операторы:
-- COUNT() - агрегатная функция для подсчета
-- WHERE - фильтрация данных перед агрегацией
-- =====================================================

SELECT 
  COUNT(courier_id) AS couriers 
FROM 
  couriers 
WHERE 
  sex = 'female'
