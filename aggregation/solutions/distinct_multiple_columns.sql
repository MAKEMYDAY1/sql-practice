-- =====================================================
-- Задача: Поиск уникальных пар курьер-заказ
-- Тема: Агрегация данных в SQL
-- Цель: Найти все уникальные комбинации курьеров и заказов, над которыми они работали

-- Используемые операторы:
-- DISTINCT с несколькими колонками - получение уникальных комбинаций
-- ORDER BY с несколькими полями - комплексная сортировка
-- =====================================================

SELECT 
  DISTINCT courier_id, 
  order_id 
FROM 
  courier_actions 
ORDER BY 
  courier_id, 
  order_id
