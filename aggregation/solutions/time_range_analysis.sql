-- =====================================================
-- Задача: Анализ временного диапазона доставок
-- Тема: Агрегация данных в SQL
-- Цель: Определить временные границы операционной деятельности службы доставки

-- Используемые функции:
-- MIN() - нахождение самой ранней временной метки
-- MAX() - нахождение самой поздней временной метки
-- WHERE - фильтрация только успешных доставок
-- =====================================================

SELECT 
  MIN(time) AS first_delivery, 
  MAX(time) AS last_delivery 
FROM 
  courier_actions 
WHERE 
  action = 'deliver_order'
