-- =====================================================
-- Задача: Поиск самых молодых пользователей мужского пола
-- Тема: Фильтрация данных в SQL
-- Цель: Найти 50 самых молодых пользователей мужского пола с указанной датой рождения

-- Используемые операторы:
-- WHERE с комбинированными условиями
-- IS NOT NULL - исключение записей с отсутствующими данными
-- ORDER BY DESC - сортировка по убыванию для нахождения самых молодых
-- LIMIT - ограничение количества результатов
-- =====================================================

SELECT 
  user_id, 
  birth_date 
FROM 
  users 
WHERE 
  sex = 'male' 
  AND birth_date IS NOT NULL 
ORDER BY 
  birth_date DESC 
LIMIT 
  50
