-- =====================================================
-- Задача: Получение заказов за конкретный месяц
-- Тема: Фильтрация данных в SQL
-- Цель: Найти все заказы, созданные пользователями в августе 2022 года

-- Используемые операторы:
-- BETWEEN - фильтрация по диапазону дат
-- AND - комбинирование условий
-- ORDER BY - сортировка результатов

-- =====================================================

SELECT 
  order_id 
FROM 
  user_actions 
WHERE 
  time BETWEEN '2022-08-01' 
  AND '2022-09-01' 
  AND action = 'create_order' 
ORDER BY 
  order_id