-- =====================================================
-- Задача: Фильтрация товаров по цене со скидкой
-- Тема: Фильтрация данных в SQL
-- Цель: Найти товары, которые после 20% скидки остаются дороже 100 рублей

-- Используемые операторы:
-- Условия WHERE с арифметическими операциями
-- =====================================================

SELECT 
  product_id, 
  name, 
  price AS old_price, 
  price * 0.8 AS new_price 
FROM 
  products 
WHERE 
  price * 0.8 > 100 
ORDER BY 
  product_id