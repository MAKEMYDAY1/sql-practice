-- =====================================================
-- Задача: Фильтрация действий пользователей по дате и типу
-- Тема: Фильтрация данных в SQL
-- Цель: Найти заказы созданные после полуночи 6 сентября 2022 года

-- Используемые операторы:
-- WHERE с несколькими условиями через AND
-- =====================================================

SELECT 
  user_id, 
  order_id, 
  time 
FROM 
  user_actions 
WHERE 
  time > '2022-09-06' 
  AND action = 'create_order' 
ORDER BY 
  order_id

-- Логика решения:
-- 1. time > '2022-09-06' - выбирает записи после полуночи 6 сентября
--    (в PostgreSQL '2022-09-06' интерпретируется как '2022-09-06 00:00:00')
-- 2. action = 'create_order' - фильтрует только действия создания заказов
-- 3. ORDER BY order_id - сортировка для последовательного анализа

-- Пример результата:
-- user_id | order_id | time
-- 115     | 10025    | 2022-09-06 08:30:15
-- 123     | 10026    | 2022-09-06 09:45:22
-- 147     | 10027    | 2022-09-06 10:15:08