-- =====================================================
-- Задача: Фильтрация пользователей по полу с ограничением результатов
-- Тема: Фильтрация данных в SQL
-- Цель: Вывести ID пользователей женского пола с ограничением в 1000 записей

-- Используемые операторы:
-- WHERE - фильтрация по условию
-- ORDER BY - сортировка результатов
-- LIMIT - ограничение количества возвращаемых строк
-- =====================================================

SELECT 
  user_id 
FROM 
  users 
WHERE 
  sex = 'female' 
ORDER BY 
  user_id 
LIMIT 
  1000
