-- =====================================================
-- Задача: Фильтрация курьеров по диапазону годов рождения
-- Тема: Фильтрация данных в SQL
-- Цель: Найти курьеров, родившихся в период с 1990 по 1995 год включительно

-- Используемые функции:
-- DATE_PART - извлечение года из даты рождения
-- BETWEEN - фильтрация по диапазону значений
-- ORDER BY - сортировка результатов

-- =====================================================

SELECT 
  courier_id 
FROM 
  couriers 
WHERE 
  DATE_PART('year', birth_date) BETWEEN 1990 
  AND 1995 
ORDER BY 
  courier_id
