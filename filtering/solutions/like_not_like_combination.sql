-- =====================================================
-- Задача: Поиск товаров по первому символу и количеству слов
-- Тема: Фильтрация данных в SQL
-- Цель: Найти товары, названия которых начинаются на "с" и состоят из одного слова

-- Используемые операторы:
-- LIKE с положительным условием
-- NOT LIKE с отрицательным условием  
-- AND - комбинирование условий
-- =====================================================

SELECT 
  product_id, 
  name 
FROM 
  products 
WHERE 
  name LIKE 'с%' 
  AND name NOT LIKE '% %' 
ORDER BY 
  product_id

-- Логика решения:
-- 1. name LIKE 'с%' - отбирает товары, названия которых начинаются на "с"
--    • "сахар" → ✓ подходит
--    • "соль" → ✓ подходит
--    • "чай" → ✗ не подходит
-- 2. name NOT LIKE '% %' - исключает товары, содержащие пробелы
--    • "сахар" → ✓ подходит (нет пробелов)
--    • "соль морская" → ✗ не подходит (есть пробел)
--    • "сок яблочный" → ✗ не подходит (есть пробел)

-- Пример результата:
-- product_id | name
-- 15         | сахар
-- 23         | соль
-- 47         | сыр
-- 52         | суп