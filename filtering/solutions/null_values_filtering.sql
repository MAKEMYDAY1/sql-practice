-- =====================================================
-- Задача: Фильтрация записей с отсутствующими данными
-- Тема: Фильтрация данных в SQL
-- Цель: Найти курьеров с неуказанной датой рождения

-- Используемые операторы:
-- IS NULL - проверка на отсутствие значения
-- ORDER BY - сортировка результатов
-- =====================================================

SELECT 
  birth_date, 
  courier_id, 
  sex 
FROM 
  couriers 
WHERE 
  birth_date IS NULL 
ORDER BY 
  courier_id
