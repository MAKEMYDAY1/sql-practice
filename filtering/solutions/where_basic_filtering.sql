-- =====================================================
-- Задача: Фильтрация товаров по цене
-- Тема: Фильтрация данных в SQL
-- Цель: Вывести товары с ценой не более 100 рублей

-- Используемые операторы:
-- WHERE - фильтрация строк по условию
-- <= - оператор сравнения "меньше или равно"
-- ORDER BY - сортировка результатов
-- =====================================================



SELECT 
  * 
FROM 
  products 
WHERE 
  price <= 100 
ORDER BY 
  product_id