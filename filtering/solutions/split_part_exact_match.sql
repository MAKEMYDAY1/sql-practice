-- =====================================================
-- Задача: Поиск товаров по первому слову или длине названия
-- Тема: Фильтрация данных в SQL
-- Цель: Найти товары, у которых первое слово в названии "чай" или название состоит из 5 символов

-- Используемые функции:
-- SPLIT_PART - извлечение первого слова из названия
-- LENGTH - подсчет длины строки
-- =====================================================

SELECT 
  product_id, 
  name 
FROM 
  products 
WHERE 
  SPLIT_PART(name, ' ', 1) = 'чай' 
  OR LENGTH(name) = 5 
ORDER BY 
  product_id