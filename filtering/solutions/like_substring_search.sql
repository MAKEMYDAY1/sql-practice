-- =====================================================
-- Задача: Поиск товаров по вхождению подстроки в название
-- Тема: Фильтрация данных в SQL
-- Цель: Найти все товары, в названии которых встречается слово "чай"

-- Используемые операторы:
-- LIKE с % - поиск подстроки в любой позиции
-- ORDER BY - сортировка результатов
-- =====================================================

SELECT 
  product_id, 
  name 
FROM 
  products 
WHERE 
  name LIKE '%чай%' 
ORDER BY 
  product_id
