-- =====================================================
-- Задача: Создание декартова произведения первых 100 пользователей и всех товаров
-- Тема: CROSS JOIN для генерации всех возможных комбинаций

-- Цель: Сгенерировать матрицу "пользователь-товар" для анализа потенциальных рекомендаций

-- Используемые операторы:
-- CROSS JOIN - декартово произведение двух таблиц
-- Подзапрос с LIMIT - ограничение выборки пользователей
-- ORDER BY - сортировка результатов
-- =====================================================
SELECT 
    u.user_id, 
    p.name 
FROM 
    (SELECT user_id FROM users ORDER BY user_id LIMIT 100) AS u
CROSS JOIN 
    products AS p
ORDER BY 
    u.user_id, 
    p.name