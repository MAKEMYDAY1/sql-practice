-- =====================================================
-- Задача: Подсчёт уникальных дат рождения из таблиц пользователей и курьеров
-- Тема: UNION для объединения наборов данных с автоматической дедупликацией

-- Цель: Определить общее количество уникальных дат рождения в системе

-- Используемые операторы:
-- UNION - объединение результатов двух запросов с удалением дубликатов
-- COUNT - агрегация для подсчёта количества записей
-- Подзапрос в FROM - создание временного набора данных
-- =====================================================
SELECT 
  COUNT(birth_date) AS dates_count 
FROM 
  (
    SELECT 
      birth_date 
    FROM 
      users 
    WHERE 
      birth_date IS NOT NULL 
    UNION 
    SELECT 
      birth_date 
    FROM 
      couriers 
    WHERE 
      birth_date IS NOT NULL
  ) t1
