-- =====================================================
-- Задача: Анализ распределения действий пользователей
-- Тема: Группировка данных в SQL
-- Цель: Посчитать количество созданных и отмененных заказов

-- Используемые операторы:
-- GROUP BY - группировка по типу действия
-- COUNT() - подсчет количества заказов в каждой группе
-- ORDER BY - сортировка по возрастанию количества
-- =====================================================

SELECT 
  action, 
  COUNT(order_id) AS orders_count 
FROM 
  user_actions 
GROUP BY 
  action 
ORDER BY 
  COUNT(order_id)
