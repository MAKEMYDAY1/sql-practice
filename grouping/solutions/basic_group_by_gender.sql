-- =====================================================
-- Задача: Анализ распределения курьеров по полу
-- Тема: Группировка данных в SQL
-- Цель: Посчитать количество курьеров мужского и женского пола

-- Используемые операторы:
-- GROUP BY - группировка строк по значениям колонки
-- COUNT() - агрегатная функция для подсчета
-- ORDER BY - сортировка результатов
-- =====================================================

SELECT 
  sex, 
  COUNT(courier_id) AS couriers_count 
FROM 
  couriers 
GROUP BY 
  sex 
ORDER BY 
  COUNT(courier_id)