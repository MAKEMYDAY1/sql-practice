-- =====================================================
-- Задача: Демонстрация работы функции UNNEST с массивами товаров
-- Тема: Функции для работы с массивами в PostgreSQL

-- Цель: Изучить преобразование массивов в отдельные строки с помощью функции UNNEST
-- =====================================================
SELECT 
  creation_time, 
  order_id, 
  product_ids, 
  unnest(product_ids) AS product_id 
FROM 
  orders 
LIMIT 
  100
