-- =====================================================
-- Задача: Вывод всех товаров кроме самого дешёвого
-- Тема: Скалярные подзапросы в WHERE
-- Цель: Отфильтровать таблицу товаров, исключив товар с минимальной ценой

-- Используемые операторы:
-- Скалярный подзапрос в WHERE - получение минимальной цены
-- != (или <>) - оператор неравенства для фильтрации
-- ORDER BY DESC - сортировка по убыванию идентификатора
-- =====================================================

SELECT 
  product_id, 
  name, 
  price 
FROM 
  products 
WHERE 
  price != (
    SELECT 
      MIN(price) 
    FROM 
      products
  ) 
ORDER BY 
  product_id DESC
